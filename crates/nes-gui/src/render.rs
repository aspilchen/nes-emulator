const WIDTH: usize = 256;
const HIGHT: usize = 240;

pub struct RenderFrame {
    pub data: Vec<u8>,
}

pub struct Renderer {
    canvas: Canvas<Window>,
}

impl RenderFrame {
    pub fn new() -> Self {
        RenderFrame {
            data: vec![0; (WIDTH) * (HIGHT) * 3],
        }
    }

    pub fn set_pixel(&mut self, x: usize, y: usize, rgb: (u8, u8, u8)) {
        let base = y * 3 * WIDTH + x * 3;
        if base + 2 < self.data.len() {
            self.data[base] = rgb.0;
            self.data[base + 1] = rgb.1;
            self.data[base + 2] = rgb.2;
        }
    }

    pub fn from_nes_frame(frame: &Frame) -> Self {
        let mut result = Self::new();
        for y in 0..HEIGHT {
            for x in 0..WIDTH {
                let index = frame.data[y][x] as usize;
                let rgb = SYSTEM_PALETTE[index];
                // let rgb = match value {
                //     0 => SYSTEM_PALETTE[0x01],
                //     1 => SYSTEM_PALETTE[0x23],
                //     2 => SYSTEM_PALETTE[0x27],
                //     3 => SYSTEM_PALETTE[0x30],
                //     _ => panic!("can't be"),
                // };
                result.set_pixel(x, y, rgb);
            }
        }
        result
    }
}

impl Renderer {
    pub fn new() -> Self {
        let sdl = sdl2::init().unwrap();
        let video = sdl.video().unwrap();
        let window = video
            .window("NES", WIDTH as u32 * 3, HEIGHT as u32 * 3)
            .position_centered()
            .build()
            .unwrap();
        let mut canvas = window.into_canvas().present_vsync().build().unwrap();
        // canvas.set_scale(10.0, 10.0).unwrap();
        Renderer { canvas }
    }
}

impl Renderer {
    pub fn render_frame(&mut self, frame: &Frame) {
        let texture_creator = self.canvas.texture_creator();
        let mut texture = texture_creator
            .create_texture_streaming(PixelFormatEnum::RGB24, WIDTH as u32, HEIGHT as u32)
            .unwrap();
        let frame = RenderFrame::from_nes_frame(frame);
        texture.update(None, &frame.data, 256 * 3).unwrap();
        self.canvas.clear();
        self.canvas.copy(&texture, None, None).unwrap();
        self.canvas.present();
    }
}
